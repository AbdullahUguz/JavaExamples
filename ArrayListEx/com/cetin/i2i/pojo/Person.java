package com.cetin.i2i.pojo;

/**
 * A class to provide keep the person information
 * @author mehmetali.cetin
 *
 */
public class Person {

	private String name;
	private short age;
	

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public short getAge() {
		return age;
	}

	public void setAge(short age) {
		this.age = age;
	}

}
